int	ft_atoi(char *s1)
{
	int	i;
	int	signe;
	int	sum;
	int v;

	i = 0;
	v = 0;
	sum = 0;
	signe = 1;
	while(s1[i] == ' ' || s1[i] == '\v' || s1[i] == '\r' || s1[i] == '\t' || s1[i] == '\n' || s1[i] == '\f' )
		i++;
	while(s1[i] == '-' || s1[i] == '+' )
	{
		if (s1[i] == '-')
			signe = signe * -1;
		i++;
		v++;
	}
	if( v > 1)
		return(0);
	while(s1[i] >= '0' && s1[i]<= '9')
	{
		sum = sum * 10 + s1[i] - 48;
		i++;
	}
	return (sum * signe);
}
